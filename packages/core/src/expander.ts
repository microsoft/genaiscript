const systemFence = "---"

function stringLikeToFileName(f: string | LinkedFile) {
    return typeof f === "string" ? f : f?.filename
}

async function callExpander(
    r: PromptTemplate,
    vars: ExpansionVariables,
    trace: MarkdownTrace,
    options: RunTemplateOptions
) {
    const cancellationToken = options?.cancellationToken
    const scope: PromptNode[] = [{ children: [] }]

    const model = r.model || DEFAULT_MODEL
    let success = true
    const parsers = createParsers({ trace, model })
    const YAML: YAML = {
        stringify: YAMLStringify,
        parse: YAMLParse,
    }
    const path = host.path
    const fs = host.fs
    const env = new Proxy(vars, {
        get: (target: any, prop, recv) => {
            const v = target[prop]
            if (v === undefined) {
                trace.error(`\`env.${String(prop)}\` not defined`)
                return ""
            }
            return v
        },
    })

    const retreival: Retreival = {
        webSearch: async (q) => {
            try {
                trace.startDetails(`üåê retreival web search \`${q}\``)
                const { webPages } = (await bingSearch(q, { trace })) || {}
                return <SearchResult>{
                    webPages: webPages?.value?.map(
                        ({ url, name, snippet }) =>
                            <LinkedFile>{
                                filename: url,
                                label: name,
                                content: snippet,
                            }
                    ),
                }
            } catch (e) {
                trace.error(`web search error`, e)
                return undefined
            } finally {
                trace.endDetails()
            }
        },
        search: async (q, files, options) => {
            try {
                trace.startDetails(`üîç retreival search \`${q}\``)
                if (!files?.length) {
                    trace.error("no files provided")
                    return { files: [], fragments: [] }
                } else {
                    await upsert(files, { trace })
                    const res = await search(q, {
                        files: files.map(stringLikeToFileName),
                        topK: options?.topK,
                    })
                    trace.fence(res, "yaml")
                    return res
                }
            } finally {
                trace.endDetails()
            }
        },
        outline: async (files) => {
            try {
                trace.startDetails(
                    `ü´• retreival outline (${files?.length || 0} files)`
                )
                const res = await outline(files, { trace })
                return res?.response
            } finally {
                trace.endDetails()
            }
        },
    }

    const defImages = (files: StringLike, options?: DefImagesOptions) => {
        const { detail } = options || {}
        if (Array.isArray(files))
            files.forEach((file) => defImages(file, options))
        else if (typeof files === "string")
            appendChild(scope[0], createImageNode({ url: files, detail }))
        else {
            const file: LinkedFile = files
            appendChild(
                scope[0],
                createImageNode(
                    (async () => {
                        let bytes: Uint8Array
                        if (/^https?:\/\//i.test(file.filename)) {
                            const resp = await fetch(file.filename)
                            if (!resp.ok) return undefined
                            const buffer = await resp.arrayBuffer()
                            bytes = new Uint8Array(buffer)
                        } else {
                            bytes = new Uint8Array(
                                await host.readFile(file.filename)
                            )
                        }
                        const mime = (await fileTypeFromBuffer(bytes))?.mime
                        if (
                            !mime ||
                            !/^image\/(png|jpeg|webp|gif)$/i.test(mime)
                        )
                            return undefined
                        const b64 = toBase64(bytes)
                        return { url: `data:${mime};base64,${b64}`, detail }
                    })()
                )
            )
        }
    }

    const defSchema = (
        name: string,
        schema: JSONSchema,
        options?: DefSchemaOptions
    ) => {
        trace.detailsFenced(
            `üß¨ schema ${name}`,
            JSON.stringify(schema, null, 2),
            "json"
        )
        appendPromptChild(createSchemaNode(name, schema, options))
    }

    const runPrompt: (
        generator: () => Promise<void>,
        promptOptions?: ModelOptions
    ) => Promise<RunPromptResult> = async (generator, promptOptions) => {
        try {
            trace.startDetails(`üéÅ run prompt`)
            const node: PromptNode = { children: [] }
            const model =
                promptOptions?.model ||
                r.model ||
                options.model ||
                DEFAULT_MODEL
            try {
                scope.unshift(node)
                await generator()
            } finally {
                scope.shift()
            }

            if (cancellationToken?.isCancellationRequested)
                return { text: "Prompt cancelled" }

            // expand template
            const { prompt, images, errors } = await renderPromptNode(
                model,
                node,
                {
                    trace,
                }
            )
            trace.fence(prompt, "markdown")
            if (images?.length || errors?.length)
                trace.fence({ images, errors }, "yaml")

            // call LLM
            const completer = options.getChatCompletions || getChatCompletions
            const res = await completer(
                {
                    model,
                    temperature:
                        promptOptions?.temperature ??
                        r.temperature ??
                        options.temperature ??
                        DEFAULT_TEMPERATURE,
                    top_p: promptOptions?.topP ?? r.topP ?? options.topP,
                    max_tokens:
                        promptOptions?.maxTokens ??
                        r.maxTokens ??
                        options.maxTokens,
                    seed: promptOptions?.seed ?? r.seed ?? options.seed,
                    stream: true,
                    messages: [toChatCompletionUserMessage(prompt, images)],
                },
                { ...options, trace }
            )
            trace.details("output", res.text)
            return { text: res.text }
        } finally {
            trace.endDetails()
        }
    }
    const appendPromptChild = (node: PromptNode) => appendChild(scope[0], node)

    let logs = ""
    let text = ""
    let images: PromptImage[] = []
    let schemas: Record<string, JSONSchema> = {}
    let functions: ChatFunctionCallback[] = []
    let fileMerges: FileMergeHandler[] = []
    try {
        await evalPrompt(
            {
                scope,
                script: () => {},
                system: () => {},
                env,
                path,
                fs,
                parsers,
                YAML,
                retreival,
                defImages,
                defSchema,
                defData: (name, data, options) =>
                    appendPromptChild(
                        createDefDataNode(name, data, env, options)
                    ),
                appendPromptChild,
                writeText: (body) => {
                    appendPromptChild(
                        createTextNode(
                            body.replace(/\n*$/, "").replace(/^\n*/, "")
                        )
                    )
                    const idx = body.indexOf(vars.error)
                    if (idx >= 0) {
                        const msg = body
                            .slice(idx + vars.error.length)
                            .replace(/\n[^]*/, "")
                        throw new Error(msg)
                    }
                },
                runPrompt,
                fetchText: async (urlOrFile, options) => {
                    if (typeof urlOrFile === "string") {
                        urlOrFile = {
                            label: urlOrFile,
                            filename: urlOrFile,
                            content: "",
                        }
                    }
                    const url = urlOrFile.filename
                    let ok = false
                    let status = 404
                    let text: string
                    if (/^https?:\/\//i.test(url)) {
                        const resp = await fetch(url, options)
                        ok = resp.ok
                        status = resp.status
                        if (ok) text = await resp.text()
                    } else {
                        try {
                            text = await readText("workspace://" + url)
                            ok = true
                        } catch (e) {
                            logVerbose(e)
                            ok = false
                            status = 404
                        }
                    }
                    const file: LinkedFile = {
                        label: urlOrFile.label,
                        filename: urlOrFile.label,
                        content: text,
                    }
                    return {
                        ok,
                        status,
                        text,
                        file,
                    }
                },
            },
            r.jsSource,
            (msg) => {
                logs += msg + "\n"
            }
        )
        assert(scope.length === 1, "scope stack should have 1 root")
        const {
            prompt,
            images: imgs,
            errors,
            schemas: schs,
            functions: fns,
            fileMerges: fms,
        } = await renderPromptNode(model, scope[0], { trace })
        text = prompt
        images = imgs
        schemas = schs
        functions = fns
        fileMerges = fms
        for (const error of errors) trace.error(``, error)
    } catch (e) {
        success = false
        if (isCancelError(e)) {
            trace.log(`cancelled: ${(e as Error).message}`)
        } else {
            const m = /at eval.*<anonymous>:(\d+):(\d+)/.exec(e.stack)
            const info = m ? ` at prompt line ${m[1]}, column ${m[2]}` : ""
            trace.error(info, e)
        }
    }

    return { logs, success, text, images, schemas, functions, fileMerges }
}

async function expandTemplate(
    template: PromptTemplate,
    fragment: Fragment,
    options: RunTemplateOptions,
    env: ExpansionVariables,
    trace: MarkdownTrace
) {
    const cancellationToken = options?.cancellationToken
    const { jsSource } = template

    traceVars()
    trace.detailsFenced("üìÑ spec", env.spec.content, "markdown")
    trace.startDetails("üíæ script")

    trace.startDetails("üß¨ prompt")
    trace.detailsFenced("üìì script source", template.jsSource, "js")
    const prompt = await callExpander(template, env, trace, options)
    const expanded = prompt.text
    const images = prompt.images
    const schemas = prompt.schemas
    const functions = prompt.functions
    const fileMerges = prompt.fileMerges

    let success = prompt.success
    const model =
        options.model ?? env.vars["model"] ?? template.model ?? DEFAULT_MODEL
    const temperature =
        options.temperature ??
        tryParseFloat(env.vars["temperature"]) ??
        template.temperature ??
        DEFAULT_TEMPERATURE
    const topP =
        options.topP ??
        tryParseFloat(env.vars["top_p"]) ??
        template.topP ??
        defaultTopP
    const max_tokens =
        options.maxTokens ??
        tryParseInt(env.vars["maxTokens"]) ??
        template.maxTokens ??
        defaultMaxTokens
    let seed =
        options.seed ??
        tryParseInt(env.vars["seed"]) ??
        template.seed ??
        defaultSeed
    if (seed !== undefined) seed = seed >> 0

    let responseType = template.responseType

    if (prompt.logs?.length) trace.details("üìù console.log", prompt.logs)
    if (model) trace.item(`model: \`${model || ""}\``)
    trace.item(
        `tokens: ${estimateTokens(template.model || DEFAULT_MODEL, expanded)}`
    )
    if (temperature !== undefined) trace.item(`temperature: ${temperature}`)
    if (topP !== undefined) trace.item(`top_p: ${topP}`)
    if (max_tokens !== undefined) trace.item(`max tokens: ${max_tokens}`)
    trace.fence(expanded, "markdown")
    trace.endDetails()

    if (cancellationToken?.isCancellationRequested) return { success: false }

    let systemText = ""
    const systems = (template.system ?? []).slice(0)
    if (template.system === undefined) {
        systems.push("system")
        systems.push("system.explanations")
        // select file expansion type
        if (/diff/i.test(jsSource)) systems.push("system.diff")
        else if (/changelog/i.test(jsSource)) systems.push("system.changelog")
        else systems.push("system.files")
        if (/annotations?/i.test(jsSource)) systems.push("system.annotations")
        if (/defschema/i.test(jsSource)) systems.push("system.schema")
    }
    for (let i = 0; i < systems.length && success; ++i) {
        if (cancellationToken?.isCancellationRequested)
            return { success: false }

        let systemTemplate = systems[i]
        let system = fragment.file.project.getTemplate(systemTemplate)
        if (!system) {
            if (systemTemplate) trace.error(`\`${systemTemplate}\` not found\n`)
            if (i > 0) continue
            systemTemplate = "system"
            system = fragment.file.project.getTemplate(systemTemplate)
            assert(!!system)
        }

        trace.startDetails(`üëæ ${systemTemplate}`)
        const sysr = await callExpander(system, env, trace, options)
        const sysex = sysr.text
        success = success && sysr.success
        if (!success) break
        if (sysr.images) images.push(...sysr.images)
        if (sysr.schemas) Object.assign(schemas, sysr.schemas)
        if (sysr.functions) functions.push(...sysr.functions)
        if (sysr.fileMerges) fileMerges.push(...sysr.fileMerges)
        systemText += systemFence + "\n" + sysex + "\n"

        responseType = responseType ?? system.responseType
        if (sysr.logs?.length) trace.details("üìù console.log", sysr.logs)
        trace.item(
            `tokens: ${estimateTokens(model || template.model || DEFAULT_MODEL, sysex)}`
        )

        trace.fence(system.jsSource, "js")
        trace.heading(3, "expanded")
        trace.fence(sysex, "markdown")
        trace.endDetails()
    }

    {
        trace.startDetails("‚öôÔ∏è configuration")
        trace.item(`model: \`${model || ""}\``)
        trace.item(`tokens: ${estimateTokens(model, expanded)}`)
        if (temperature !== undefined) trace.item(`temperature: ${temperature}`)
        if (topP !== undefined) trace.item(`top_p: ${topP}`)
        if (max_tokens !== undefined) trace.item(`max tokens: ${max_tokens}`)
        if (seed !== undefined) trace.item(`seed: ${seed}`)
        if (responseType) trace.item(`response type: ${responseType}`)
        trace.endDetails() // expanded prompt
    }

    trace.endDetails()

    return {
        expanded,
        images,
        schemas,
        functions,
        success,
        model,
        temperature,
        topP,
        max_tokens,
        seed,
        systemText,
        responseType,
        fileMerges,
    }

    function tryParseInt(v: string) {
        const i = parseInt(v)
        return isNaN(i) ? undefined : i
    }

    function tryParseFloat(v: string) {
        const i = parseFloat(v)
        return isNaN(i) ? undefined : i
    }

    function traceVars() {
        const varName: Record<string, string> = {}
        for (const [k, v] of Object.entries(env)) {
            if (!varName[v]) varName[v] = k
        }
        const varMap = env as any as Record<string, string | any[]>

        trace.startDetails("üé∞ variables")
        trace.tip("Variables are referenced through `env.NAME` in prompts.")

        const files = env.files
        if (files?.length) {
            trace.item(`env.**files**:`)
            for (const file of files) {
                const { filename, content } = file
                trace.detailsFenced(
                    `${file.filename}, ${content?.length || 0} chars`,
                    inspect(file.content)
                )
            }
        }

        const keys = Object.keys(env).filter((k) => k !== "files")
        for (const k of keys.filter((k) => !isComplex(k))) {
            const v = varMap[k]
            if (typeof v === "string" && varName[v] != k)
                trace.item(`env.**${k}**: same as **${varName[v]}**`)
            else trace.item(`env.**${k}**: \`${v}\``)
        }

        for (const k of keys.filter(isComplex)) {
            const v = varMap[k]
            trace.item(`env.**${k}**`)
            trace.fence(
                typeof v === "string" ? v : inspect(v),
                typeof v === "string" ? undefined : "js"
            )
        }
        trace.endDetails()

        function isComplex(k: string) {
            const v = varMap[k]
            if (typeof v === "string" && varName[v] != k) return false
            return (
                typeof v !== "string" ||
                v.length > 40 ||
                v.trim().includes("\n") ||
                v.includes("`")
            )
        }
    }
}

async function fragmentVars(
    trace: MarkdownTrace,
    template: PromptTemplate,
    frag: Fragment
) {
    const { file } = frag
    const project = file.project
    const prjFolder = host.projectFolder()

    const files: LinkedFile[] = []
    for (const fr of allChildren(frag, true)) {
        for (const ref of fr.references) {
            // what about URLs?
            if (/^https:\/\//.test(ref.filename)) {
                if (!files.find((lk) => lk.filename === ref.filename)) {
                    let content: string = ""
                    try {
                        const urlAdapters = defaultUrlAdapters.concat(
                            template.urlAdapters ?? []
                        )
                        let url = ref.filename
                        let adapter: UrlAdapter = undefined
                        for (const a of urlAdapters) {
                            const newUrl = a.matcher(url)
                            if (newUrl) {
                                url = newUrl
                                adapter = a
                                break
                            }
                        }
                        trace.item(`fetch ${url}`)
                        const resp = await fetch(url, {
                            headers: {
                                "Content-Type":
                                    adapter?.contentType ?? "text/plain",
                            },
                        })
                        trace.item(`status: ${resp.status}, ${resp.statusText}`)
                        if (resp.ok)
                            content =
                                adapter?.contentType === "application/json"
                                    ? adapter.adapter(await resp.json())
                                    : await resp.text()
                    } catch (e) {
                        trace.error(`fetch def error`, e)
                    }
                    files.push({
                        label: ref.name,
                        filename: ref.filename,
                        content,
                    })
                }
                continue
            }

            // check for existing file
            const projectFile = project.allFiles.find(
                (f) => f.filename === ref.filename
            )
            if (!projectFile) {
                trace.error(`reference ${ref.filename} not found`)
                continue
            }

            const fn = relativePath(host.projectFolder(), projectFile.filename)
            if (!files.find((lk) => lk.filename === fn))
                files.push({
                    label: ref.name,
                    filename: fn,
                    content: projectFile.content,
                })
        }
    }
    const parents: LinkedFile[] = []
    if (frag.parent)
        parents.push({
            label: frag.parent.title,
            filename: relativePath(prjFolder, frag.parent.file.filename),
            content: frag.parent.file.content,
        })
    const attrs = commentAttributes(frag)
    const secrets: Record<string, string> = {}
    for (const secret of template.secrets || []) {
        const value = await host.readSecret(secret)
        if (value) {
            trace.item(`secret \`${secret}\` used`)
            secrets[secret] = value
        } else trace.error(`secret \`${secret}\` not found`)
    }
    const vars: Partial<ExpansionVariables> = {
        ...staticVars(),
        spec: {
            filename: relativePath(host.projectFolder(), file.filename),
            label: "context",
            content: file.content,
        },
        files,
        parents,
        template: {
            id: template.id,
            title: template.title,
            description: template.description,
        },
        vars: attrs,
        secrets,
    }
    return vars
}

export type RunTemplateOptions = ChatCompletionsOptions &
    ModelOptions & {
        cancellationToken?: CancellationToken
        infoCb?: (partialResponse: {
            text: string
            label?: string
            summary?: string
            vars?: Partial<ExpansionVariables>
        }) => void
        trace?: MarkdownTrace
        maxCachedTemperature?: number
        maxCachedTopP?: number
        skipLLM?: boolean
        label?: string
        cache?: boolean
        cliInfo?: {
            spec: string
        }
        chat?: ChatAgentContext
        getChatCompletions?: (
            req: CreateChatCompletionRequest,
            options?: ChatCompletionsOptions & { trace: MarkdownTrace }
        ) => Promise<ChatCompletionResponse>
        vars?: Record<string, string>
    }

export function generateCliArguments(
    template: PromptTemplate,
    fragment: Fragment,
    options: RunTemplateOptions
) {
    const { model, temperature, topP, seed, cliInfo } = options

    const cli = [
        "node",
        GENAISCRIPT_CLI_JS,
        "run",
        template.id,
        cliInfo.spec,
        "--apply-edits",
    ]
    if (model) cli.push(`--model`, model)
    if (!isNaN(temperature)) cli.push(`--temperature`, temperature + "")
    if (!isNaN(topP)) cli.push(`--top-p`, topP + "")
    if (!isNaN(seed)) cli.push("--seed", seed + "")

    return cli.join(" ")
}

export async function runTemplate(
    template: PromptTemplate,
    fragment: Fragment,
    options: RunTemplateOptions
): Promise<FragmentTransformResponse> {
    assert(fragment !== undefined)
    const {
        requestOptions = {},
        skipLLM,
        label,
        cliInfo,
        trace = new MarkdownTrace(),
    } = options || {}
    const cancellationToken = options?.cancellationToken
    const version = CORE_VERSION

    const isCancelled = () => cancellationToken?.isCancellationRequested

    trace.heading(2, label || template.id)

    if (cliInfo) traceCliArgs(trace, template, fragment, options)

    const vars = await fragmentVars(trace, template, fragment)
    // override with options vars
    if (options.vars)
        vars.vars = { ...(vars.vars || {}), ...(options.vars || {}) }
    vars.chat = options.chat || { history: [], prompt: "" }

    let {
        expanded,
        images,
        schemas,
        functions,
        fileMerges,
        success,
        temperature,
        topP,
        model,
        max_tokens,
        seed,
        systemText,
        responseType,
    } = await expandTemplate(
        template,
        fragment,
        options,
        vars as ExpansionVariables,
        trace
    )

    // initial messages (before tools)
    const messages: ChatCompletionRequestMessage[] = [
        {
            role: "system",
            content: systemText,
        },
        toChatCompletionUserMessage(expanded, images),
    ]

    // if the expansion failed, show the user the trace
    if (!success) {
        return <FragmentTransformResponse>{
            error: new Error("Template failed"),
            prompt: messages,
            vars,
            trace: trace.content,
            text: "# Template failed\nSee trace.",
            edits: [],
            annotations: [],
            changelogs: [],
            fileEdits: {},
            label,
            version,
        }
    }

    // don't run LLM
    if (skipLLM) {
        return <FragmentTransformResponse>{
            prompt: messages,
            vars,
            trace: trace.content,
            text: undefined,
            edits: [],
            annotations: [],
            changelogs: [],
            fileEdits: {},
            label,
            version,
        }
    }
    const response_format = responseType ? { type: responseType } : undefined

    const status = (text?: string) => {
        options.infoCb?.({
            vars,
            text,
            label,
        })
    }

    let text: string
    const fileEdits: Record<string, { before: string; after: string }> = {}
    const changelogs: string[] = []
    let annotations: Diagnostic[] = []
    const edits: Edits[] = []
    let summary: string = undefined
    const projFolder = host.projectFolder()
    const links: string[] = []
    const fp = fragment.file.filename
    const fragn = /^.\//.test(fp)
        ? host.resolvePath(projFolder, fragment.file.filename)
        : fp
    const ff = host.resolvePath(fp, "..")
    const refs = fragment.references
    const fragmentVirtual = await fileExists(fragment.file.filename, {
        virtual: true,
    })
    const tools: ChatCompletionTool[] = functions?.length
        ? functions.map((f) => ({
              type: "function",
              function: f.definition as any,
          }))
        : undefined
    const getFileEdit = async (fn: string) => {
        let fileEdit = fileEdits[fn]
        if (!fileEdit) {
            let before: string = null
            let after: string = undefined
            if (await fileExists(fn, { virtual: false }))
                before = await readText(fn)
            else if (await fileExists(fn, { virtual: true }))
                after = await readText(fn)
            fileEdit = fileEdits[fn] = { before, after }
        }
        return fileEdit
    }

    while (!isCancelled()) {
        let resp: ChatCompletionResponse
        try {
            try {
                status(`Prompting model ${model}`)
                trace.startDetails(
                    `üß† llm request (${messages.length} messages)`
                )
                trace.item(
                    `tokens: ${estimateChatTokens(model, messages, tools)}`
                )
                status()
                const completer =
                    options.getChatCompletions || getChatCompletions
                resp = await completer(
                    {
                        model,
                        temperature,
                        top_p: topP,
                        max_tokens,
                        seed,
                        messages,
                        stream: true,
                        response_format,
                        tools,
                    },
                    { ...options, trace }
                )
            } finally {
                trace.endDetails()
                status()
            }
        } catch (error: unknown) {
            if (error instanceof TypeError) {
                trace.heading(3, `Request error`)
                trace.item(error.message)
                if (error.cause) trace.fence(error.cause)
                if (error.stack) trace.fence(error.stack)
                resp = {
                    text: "Unexpected error",
                }
            } else if (error instanceof RequestError) {
                trace.heading(3, `Request error`)
                if (error.body) {
                    trace.log(`> ${error.body.message}\n\n`)
                    trace.item(`type: \`${error.body.type}\``)
                    trace.item(`code: \`${error.body.code}\``)
                }
                trace.item(`status: \`${error.status}\`, ${error.statusText}`)
                resp = {
                    text: `Request error: \`${error.status}\`, ${error.statusText}\n`,
                }
            } else if (isCancelled()) {
                trace.heading(3, `Request cancelled`)
                trace.log(`The user requested to cancel the request.`)
                resp = { text: "Request cancelled" }
                error = undefined
            } else {
                trace.heading(3, `Fetch error`)
                trace.error(`fetch error`, error)
                resp = { text: "Unexpected error" }
            }

            status(`error`)
            return <FragmentTransformResponse>{
                prompt: messages,
                vars,
                trace: trace.content,
                error,
                text: resp?.text,
                edits,
                annotations,
                changelogs,
                fileEdits,
                label,
                version,
            }
        }

        if (resp.text) {
            trace.startDetails("üì© llm response")
            if (resp.finishReason)
                trace.item(`finish reason: \`${resp.finishReason}\``)
            trace.detailsFenced(`output (raw)`, resp.text, "markdown")
            trace.details(`output (rendered)`, pretifyMarkdown(resp.text))
            trace.endDetails()
        }

        status()
        if (resp.toolCalls?.length) {
            if (resp.text)
                messages.push({
                    role: "assistant",
                    content: resp.text,
                })
            messages.push({
                role: "assistant",
                content: null,
                tool_calls: resp.toolCalls.map((c) => ({
                    id: c.id,
                    function: {
                        name: c.name,
                        arguments: c.arguments,
                    },
                    type: "function",
                })),
            })

            // call tool and run again
            for (const call of resp.toolCalls) {
                if (isCancelled()) break
                try {
                    status(`running tool ${call.name}`)
                    trace.startDetails(`üì† tool call ${call.name}`)
                    trace.item(`id: \`${call.id}\``)
                    trace.item(`args:`)
                    trace.fence(call.arguments)

                    const callArgs: any = call.arguments
                        ? JSON5TryParse(call.arguments)
                        : undefined
                    const fd = functions.find(
                        (f) => f.definition.name === call.name
                    )
                    if (!fd) throw new Error(`function ${call.name} not found`)

                    const context: ChatFunctionCallContext = {
                        trace,
                    }

                    let output = await fd.fn({ context, ...callArgs })
                    if (typeof output === "string") output = { content: output }
                    if (output?.type === "shell") {
                        let {
                            command,
                            args = [],
                            stdin,
                            cwd,
                            timeout,
                            ignoreExitCode,
                            files,
                            outputFile,
                        } = output
                        trace.item(
                            `shell command: \`${command}\` ${args.join(" ")}`
                        )
                        status()
                        const { stdout, stderr, exitCode } = await exec(host, {
                            trace,
                            label: call.name,
                            call: {
                                type: "shell",
                                command,
                                args,
                                stdin,
                                files,
                                outputFile,
                                cwd: cwd ?? projFolder,
                                timeout: timeout ?? 60000,
                            },
                        })
                        output = { content: stdout }
                        trace.item(`exit code: ${exitCode}`)
                        if (stdout) trace.details("üì© shell output", stdout)
                        if (stderr) trace.details("üì© shell error", stderr)
                        if (exitCode !== 0 && !ignoreExitCode)
                            throw new Error(
                                `tool ${call.name} failed with exit code ${exitCode}}`
                            )
                        status()
                    }

                    const { content, edits: functionEdits } = output

                    if (content) trace.fence(content, "markdown")
                    if (functionEdits?.length) {
                        trace.fence(functionEdits)
                        edits.push(
                            ...functionEdits.map((e) => {
                                const { filename, ...rest } = e
                                const n = e.filename
                                const fn = /^[^\/]/.test(n)
                                    ? host.resolvePath(projFolder, n)
                                    : n
                                return { filename: fn, ...rest }
                            })
                        )
                    }

                    messages.push({
                        role: "tool",
                        content,
                        tool_call_id: call.id,
                    })
                } catch (e) {
                    trace.error(`function failed`, e)
                    status(`error`)
                    throw e
                } finally {
                    trace.endDetails()
                    status()
                }
            }
        } else {
            text =
                messages
                    .filter((msg) => msg.role === "assistant" && msg.content)
                    .map((m) => m.content)
                    .join("\n") + resp.text
            break
        }
    }

    annotations = parseAnnotations(text)
    const json = JSON5TryParse(text, undefined)
    const yaml = YAMLTryParse(text, undefined, { ignoreLiterals: true })
    const fences =
        json === undefined && yaml === yaml ? extractFenced(text) : []
    const frames: DataFrame[] = []

    // validate schemas in fences
    for (const fence of fences.filter(
        ({ language }) => language === "json" || language === "yaml"
    )) {
        const { language, content: val, args } = fence
        // validate well formed json/yaml
        const data =
            language === "json" ? JSON5TryParse(val) : YAMLTryParse(val)
        if (data === undefined) {
            trace.error(`invalid ${language} syntax`)
            continue
        }
        // check if schema specified
        const schema = args?.schema
        const schemaObj = schema && schemas[schema]
        if (schema) {
            if (!schemaObj) trace.error(`schema ${schema} not found`)
            fence.validation = validateJSONWithSchema(trace, data, schemaObj)
            frames.push({
                schema,
                data,
                validation: fence.validation,
            })
        } else frames.push({ data })
    }

    if (fences?.length)
        trace.details("üì© code regions", renderFencedVariables(fences))

    if (yaml !== undefined) trace.detailsFenced("üì© yaml (parsed)", yaml)
    else if (json !== undefined) trace.detailsFenced("üì© json (parsed)", json)

    if (yaml !== undefined) {
        const fn = fragment.file.filename.replace(
            /\.gpspec\.md$/i,
            "." + template.id + ".yaml"
        )
        const fileEdit = await getFileEdit(fn)
        fileEdit.after = text
    } else if (json !== undefined) {
        const fn = fragment.file.filename.replace(
            /\.gpspec\.md$/i,
            "." + template.id + ".json"
        )
        const fileEdit = await getFileEdit(fn)
        fileEdit.after = text
    } else {
        for (const fence of fences.filter(
            ({ validation }) => validation?.valid !== false
        )) {
            const { label: name, content: val } = fence
            const pm = /^((file|diff):?)\s+/i.exec(name)
            if (pm) {
                const kw = pm[1].toLowerCase()
                const n = undoublequote(name.slice(pm[0].length).trim())
                const fn = /^[^\/]/.test(n)
                    ? host.resolvePath(projFolder, n)
                    : n
                const ffn = relativePath(ff, fn)
                const curr = refs.find((r) => r.filename === fn)?.filename

                const fileEdit = await getFileEdit(fn)
                if (kw === "file") {
                    if (fileMerges.length) {
                        try {
                            for (const fileMerge of fileMerges)
                                fileEdit.after =
                                    fileMerge(
                                        fn,
                                        label,
                                        fileEdit.after ?? fileEdit.before,
                                        val
                                    ) ?? val
                        } catch (e) {
                            logVerbose(e)
                            trace.error(`error custom merging diff in ${fn}`, e)
                        }
                    } else fileEdit.after = val
                } else if (kw === "diff") {
                    const chunks = parseLLMDiffs(val)
                    try {
                        fileEdit.after = applyLLMPatch(
                            fileEdit.after || fileEdit.before,
                            chunks
                        )
                    } catch (e) {
                        logVerbose(e)
                        trace.error(`error applying patch to ${fn}`, e)
                        try {
                            fileEdit.after = applyLLMDiff(
                                fileEdit.after || fileEdit.before,
                                chunks
                            )
                        } catch (e) {
                            logVerbose(e)
                            trace.error(`error merging diff in ${fn}`, e)
                        }
                    }
                }
                if (!curr && fragn !== fn) links.push(`-   [${ffn}](${ffn})`)
            } else if (/^changelog$/i.test(name)) {
                changelogs.push(val)
                const cls = parseChangeLogs(val)
                for (const changelog of cls) {
                    const { filename } = changelog
                    const fn = /^[^\/]/.test(filename)
                        ? host.resolvePath(projFolder, filename)
                        : filename
                    const ffn = relativePath(ff, fn)
                    const curr = refs.find((r) => r.filename === fn)?.filename

                    const fileEdit = await getFileEdit(fn)
                    fileEdit.after = applyChangeLog(
                        fileEdit.after || fileEdit.before || "",
                        changelog
                    )
                    if (!curr && fragn !== fn)
                        links.push(`-   [${ffn}](${ffn})`)
                }
            } else if (/^summary$/i.test(name)) {
                summary = val
            }
        }
    }

    // convert file edits into edits
    Object.entries(fileEdits)
        .filter(([, { before, after }]) => before !== after) // ignore unchanged files
        .forEach(([fn, { before, after }]) => {
            if (before) {
                edits.push({
                    label: `Update ${fn}`,
                    filename: fn,
                    type: "replace",
                    range: [[0, 0], stringToPos(after)],
                    text: after,
                })
            } else {
                edits.push({
                    label: `Create ${fn}`,
                    filename: fn,
                    type: "createfile",
                    text: after,
                    overwrite: true,
                })
            }
        })

    // add links to the end of the file
    if (
        links.length &&
        (!fragmentVirtual || fileEdits[fragment.file.filename]?.after)
    ) {
        const obj = {
            label: template.title,
            filename: fragment.file.filename,
        }
        edits.push({
            ...obj,
            type: "insert",
            pos: fragment.endPos,
            text: `\n${links.join("\n")}`,
        })
    }

    if (edits.length)
        trace.details(
            "‚úèÔ∏è edits",
            CSVToMarkdown(edits, { headers: ["type", "filename", "message"] })
        )
    if (annotations.length)
        trace.details(
            "‚ö†Ô∏è annotations",
            CSVToMarkdown(annotations, {
                headers: ["severity", "filename", "line", "message"],
            })
        )

    const res: FragmentTransformResponse = {
        prompt: messages,
        vars,
        edits,
        annotations,
        changelogs,
        fileEdits,
        trace: trace.content,
        text,
        summary,
        version,
        fences,
        frames,
    }
    options?.infoCb?.(res)
    return res
}

